const countryMap = new Map(countries.map((country) => [country.name.toLowerCase(), country.code]));

const populateCountryList = () => {
  if (!countryList) return;

  countryList.innerHTML = "";
  countries.forEach((country) => {
    const option = document.createElement("option");
    option.value = country.name;
    countryList.appendChild(option);
  });
};

const syncCountryCode = () => {
  if (!countryInput || !countryCodeInput) return;

  const lookup = countryInput.value.trim().toLowerCase();
  const match = countryMap.get(lookup) || "";
  countryCodeInput.value = match;

  if (countryHelper) {
    if (lookup && !match) {
      countryHelper.textContent = "No match found. Please choose a country from the list.";
    } else {
      countryHelper.textContent = "";
    }
  }
};

const showMessage = (text, type) => {
  message.textContent = text;
  message.className = `form-message ${type}`;
};

const validateForm = (data) => {
  const errors = [];

  if (!data.name) errors.push("Name is required");
  if (!data.phoneNumber) errors.push("Phone Number is required");
  if (!data.emailAddress) errors.push("Email Address is required");
  if (!data.preferredChannel) errors.push("Preferred Communication Channel is required");
  if (!data.cropType) errors.push("Crop Type is required");
  if (!data.locality) errors.push("Town / Village / Ward is required");
  if (!data.administrativeArea) errors.push("County / State / Region is required");
  if (!data.countryCode) errors.push("Country is required");

  const emailOk = /^[^@\s]+@[^@\s]+\.[^@\s]+$/.test(data.emailAddress || "");
  if (!emailOk) errors.push("Enter a valid email address");

  const phoneOk = /^\+?[0-9]{7,15}$/.test((data.phoneNumber || "").replace(/\s+/g, ""));
  if (!phoneOk) errors.push("Enter a valid phone number");

  return errors;
};

form.addEventListener("submit", async (event) => {
  event.preventDefault();

  syncCountryCode();

  const formData = new FormData(form);
  const payload = {
    name: formData.get("name")?.trim(),
    phoneNumber: formData.get("phoneNumber")?.trim(),
    emailAddress: formData.get("emailAddress")?.trim(),
    preferredChannel: formData.get("preferredChannel"),
    cropType: formData.get("cropType")?.trim(),
    telegramChatId: formData.get("telegramChatId")?.trim(),
    locality: formData.get("locality")?.trim(),
    administrativeArea: formData.get("administrativeArea")?.trim(),
    countryCode: formData.get("countryCode")?.trim()
  };

  const errors = validateForm(payload);
  if (errors.length) {
    showMessage(errors[0], "error");
    return;
  }

  submitBtn.disabled = true;
  showMessage("Submitting...", "");

  try {
    const response = await fetch(webhookUrl, {
      method: "POST",
      headers: {
        "Content-Type": "application/json"
      },
      body: JSON.stringify(payload)
    });

    const result = await response.json().catch(() => ({}));

    if (response.ok) {
      showMessage("Submission received. Thank you!", "success");
      form.reset();
    } else {
      const serverError = Array.isArray(result.errors) ? result.errors[0] : "";
      showMessage(serverError || result.message || "Failed to submit. Please try again.", "error");
    }
  } catch (error) {
    showMessage("Network error. Please retry in a moment.", "error");
  } finally {
    submitBtn.disabled = false;
  }
});

populateCountryList();
if (countryInput) {
  countryInput.addEventListener("input", syncCountryCode);
  countryInput.addEventListener("blur", syncCountryCode);
}
